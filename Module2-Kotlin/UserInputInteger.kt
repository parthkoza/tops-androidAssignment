package Assignment

//Kotlin Program to Print an Integer (Entered by the User)

fun main() {
    println("Enter a Integer : ")
    var num1 = readln()!!.toInt()
    println("Your Number is  $num1")
    println()
    println("Enter a Float value :")  // Kotlin Program to Print an Integer
    var num2 = readln()!!.toFloat()
    println("Your Number is  $num2")
    println()
    println("Enter a Float value :")  // Kotlin Program to Print an Double
    var num3 = readln()!!.toDouble()
    println("Your Number is  $num3")


}